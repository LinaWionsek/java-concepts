public class Verzweigung {
    public static void main(String[] args) {
        var höhe = 3;
        var breite = -1;

        // verzweigung
        if (höhe > 0 && breite > 0) {
            System.out.println("Alles gut!");
        }
        else {
            System.out.println("ERROR!");
        }

        System.out.println("Tschüssi!");
    }
}